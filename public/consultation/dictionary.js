export function translateToLocal(term, industry) {
  const dict = DICTIONARIES[industry] || DICTIONARIES[INDUSTRIES.OTHER];
  return dict[term] || term;
}

export function getIndustryDictionary(industry) {
  return DICTIONARIES[industry] || DICTIONARIES[INDUSTRIES.OTHER];
}

export function dictionaryToPromptText(industry) {
  const dict = getIndustryDictionary(industry);
  return Object.entries(dict)
    .map(([term, local]) => `「${term}」→「${local}」`)
    .join('\n');
}

export function quickEstimateIndustry(text) {
  const scores = {};
  for (const [industry, keywords] of Object.entries(INDUSTRY_KEYWORDS)) {
    scores[industry] = keywords.filter(kw => text.includes(kw)).length;
  }
  const best = Object.entries(scores).sort((a, b) => b[1] - a[1])[0];
  return best && best[1] > 0 ? best[0] : INDUSTRIES.OTHER;
}
